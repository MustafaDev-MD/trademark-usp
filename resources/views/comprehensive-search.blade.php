        <div class="accordion-item">
            <button class="accordion-header" aria-expanded="false" aria-controls="content-8">
                What is a common law trademark and why bother to register a mark?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-8" hidden>
                <p>Under U.S. law, a “common law trademark” is generally established when someone uses a company name, logo or slogan in commerce, even if it is not registered.</p>

                <p>So, why pay to register a trademark when a common law trademark may already exist? Common law rights ordinarily are limited to the geographic area where the mark is used as opposed to the nationwide protection customarily obtained when a mark is registered with the USPTO.</p>

                <p>So, unless registered, the use of a mark can be geographically limited, which hampers the ability to expand the brand. On the other hand, a person using a mark in a limited geographic area could be boxed in by someone else who offensively registers a similar mark.</p>

                <p>In addition, registration of a trademark can give the person holding the registered trademark a leg up in court as to the validity of the mark and the date of usage in later trademark infringement litigation, if it comes to that. There are also favorable remedies available to registered trademark owners in the event of litigation.</p>

                <p>Finally, once a trademark is accepted by the USPTO, it will be maintained in the USPTO database, which can discourage others from using the mark in the future. Future companies should be on notice that the mark is already spoken for, which should in turn help avoid at least some disputes.</p>
