        <div class="accordion-item">
            <button class="accordion-header" aria-expanded="false" aria-controls="content-8">
                What is a trademark and what does it do?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-8" hidden>
                <p>One way to understand a trademark is that it is a word, phrase, symbol, and/or design that identifies and distinguishes the source of the goods of one party from those of another party.</p>

                <p>A “service” mark distinguishes the source of a service, rather than a good, but the two are typically simply referred to as a “trademark” or “mark”. In more general terms, getting a trademark protects a brand. Many of the well-known brands, logos and slogans you love, know and trust have been registered with the United States Patent and Trademark Office.</p>

                <p>Generally, the registration of a trademark entitles the registrant to a presumption of ownership of the brand on a national level and a presumed right to use the brand nationwide. It may help prevent someone from registering a confusingly similar mark later and may also help the registrant bring a case in federal court if someone infringes on the brand. Once registered, a registrant can typically start using the ® symbol after the name, logo or slogan.</p>

                <p>After a mark is properly registered and used for a five-year period, Trademark USP can also help file a “Declaration of Incontestability.” Considered by some the greatest protection under U.S. trademark law, this may help prevent others from contesting a trademark on the following grounds:</p>

                <ul>
                    <li>the mark is not inherently distinctive;</li>
                    <li>it is confusingly similar to another mark that someone else began using first; or</li>
                    <li>the mark is simply functional as opposed to identifying the source of the goods or services.</li>
                </ul>

