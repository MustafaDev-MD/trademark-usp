        <div class="accordion-item">
            <button class="accordion-header" aria-expanded="false" aria-controls="content-6">
                What makes a good name?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-6" hidden>
                <p>The United State Patent and Trademark Office will not necessarily approve every name for a trademark. The USPTO generally describes names as “generic,” “descriptive,” “suggestive,” “arbitrary,” or “fanciful.” A trademark application is more likely to be registered by the USPTO the less “generic” or more “fanciful” it is.</p>

                <h3>Generic Marks (Least Likely to be Approved)</h3>
                <p>Generic names are rarely given protection. For example, a company that makes screwdrivers and tries to trademark the name “The Screwdriver Company” is unlikely to be successful.</p>

                <h3>Descriptive Marks</h3>
                <p>Merely descriptive names are also unlikely to receive registrations from the USPTO. For instance, “The Metal Screwdriver Company” is not likely to pass muster because it merely describes a screwdriver as being made of metal.</p>
                <p>Ordinarily, descriptive marks are entitled to trademark protection, but only if they have gained what has been termed “secondary meaning.” In other words, the name has become so famous that despite the generic nature, the public associates the product with a specific company. One example is International Business Machines or IBM. Fledgling companies generally are not well known enough to have attained a secondary meaning. Generic names generally do not attain secondary meaning, so are unlikely to be registered by the USPTO.</p>

                <h3>Suggestive Marks</h3>
                <p>The chances of approval normally improve with suggestive marks, which often hint at the quality or another aspect of the company. For example, “Herculean Screwdriver Company” may be more likely to be registered by the USPTO than “Strong Screwdriver,” but conveys a similar meaning. However, the line between a suggestive mark and a descriptive one is difficult to draw. Microsoft, which makes software for microcomputers, and Citibank, which provides financial services, are some well-known examples of suggestive marks. Brand owners often like these names because it gives the consumer a good idea of what the company does without the need for additional (and often expensive) education or advertising to disseminate the name for the particular product or service.</p>

                <h3>Arbitrary Marks</h3>
                <p>Next on the list are arbitrary marks. These are usually existing words used in a way unrelated to their normal everyday meanings. Hence, Chocolate Screwdrivers may work. The textbook example of the arbitrary mark is Apple Computers - not to be confused with Apple Records.</p>

                <h3>Fanciful Marks (Most Likely to be Approved)</h3>
                <p>Finally, fanciful marks are ordinarily the easiest marks to be approved. A fanciful mark may be a made up word or a very rarely used word that has nothing to do with describing the products. “Glotz” for a screwdriver company may work. Some examples of fanciful marks are Yahoo!, Google, Exxon and Spotify.</p>

                <p>Other common reasons the USPTO may reject names can include the following:</p>
                <ul>
                    <li>it is too similar to another name already registered.</li>
                    <li>it is a surname.</li>
                    <li>it is geographically descriptive of where you are doing business.</li>
                </ul>
