            <p class="blog-content-intro-text">
                “It’s not so bad,” you think to yourself as you copy and paste a chunk of text from somebody else’s
                article into your own. “I’m only using a little bit of the article. Plus, nobody will ever know I
                took it from somewhere else.”
            </p>
            <p>
                Except somebody does notice.
            </p>
            <p>
                Now you’ve put your business in jeopardy for what could be a massive financial penalty. If you’re
                lucky, you’ll get away with a Digital Millennium Copyright Act (DMCA) takedown request telling you
                to remove the offending piece. But if the original copyright holder wants to take things further,
                they could take you all the way to the supreme court. Your business may receive fines numbering into
                the six figures for each offense, especially if the courts decide you broke copyright law.
            </p>
            <p>
                Maybe you think that taking somebody else’s original work isn’t such a bad thing, even if it’s only
                a little bit. Or you don’t fully understand U.S. copyright law and fair use rules. Whatever the
                case, these cautionary tales of copyright infringement should make you think twice before you
                infringe on somebody else’s copyright.
            </p>

            <hr>

            <h2 class="blog-content-heading">Tale #1: Star Wars vs. Battlestar Galactica</h2>
            <p>
                The first Star Wars film came out in 1977, creating a new wave of interest in the sci-fi genre. It
                inspired many imitators and similar genre programming, including the Battlestar Galactica TV series.
            </p>
            <p>
                You might be familiar with the latter’s 2004 iteration, but did you know that was not the first
                attempt at Battlestar Galactica content? Universal aired the first Battlestar Galactica TV series
                from 1978 to 1979, running 24 episodes while battling a lawsuit from 20th Century Fox. The problem
                was that Battlestar Galactica was too close to Star Wars.
            </p>

            <h3 class="blog-content-subheading">The Infringement</h3>
            <p>
                20th Century Fox claimed in their lawsuit against Universal that Battlestar Galactica took too much
                from the Star Wars universe and could not be considered its own thing. Particular points of
                contention included a character named Skylar, which Fox thought was too close to Skywalker, the
                film’s original title, Star Worlds, and even the trails that the lasers in Battlestar Galactica gave
                out, which Fox felt were too similar to those in Star Wars.
            </p>
            <p>
                All told, Fox believed Universal was trying to use 34 distinct ideas from Star Wars in their new
                film.
            </p>
            <p>
                Universal didn’t take kindly to the copyright lawsuit. Instead of backing down, the company filed
                its own suit against Fox, alleging that Star Wars stole the idea of robot drones from its 1972 film
                *Silent Running*.
            </p>

            <h3 class="blog-content-subheading">The Outcome</h3>
            <p>
                The case was messy from the outset. However, the parties agreed on a settlement out of court after
                two years of legal wrangling. Unfortunately, *Battlestar Galactica* had already been canceled by
                that point, partly because of how long the case took. The world was already waiting for *The Empire
                Strikes Back*, and it would be almost 30 years before *Battlestar Galactica* found its way to
                screens.
            </p>
            <p>
                The lesson is that even a potential copyright infringement can be highly damaging to a business. In
                Universal’s case, having distinct similarities led to a long and costly legal case.
            </p>

            <hr>

            <h2 class="blog-content-heading">Tale #2: The Invincible Saga</h2>
            <p>
                If you’ve read the Invincible comic books or watched the first season of the animated TV series, you
                know it pulls no punches. The books offer a brutal deconstruction of classic superhero story tropes,
                making it an interesting counterpoint to the many traditional Marvel and DC films we’ve seen in
                recent years.
            </p>
            <p>
                However, *Invincible* also gives us an example of how complicated copyright law can get. An ongoing
                case between the comic’s writer, Robert Kirkman, and the colorist of the first 50 issues, William
                Crabtree, raises some interesting questions about joint ownership of a copyright.
            </p>

            <h3 class="blog-content-subheading">The Infringement</h3>
            <p>
                Crabtree alleges that his work as a colorist on *Invincible* gives him co-creator rights with
                Kirkman, which means he should have a say in what happens to the series and how he benefits from it.
                Kirkman had him sign a contract that would supposedly make it easier to license the comic while
                protecting Crabtree’s rights. More interestingly, Crabtree also claims they had an oral agreement to
                give Crabtree a portion of the TV series’ revenue.
            </p>
            <p>
                Instead, the contract characterized Crabtree’s contributions to the comic series as “work for hire.”
                When Kirkman licensed *Invincible* to Amazon Studios for a TV series, Crabtree was not part of the
                negotiation.
            </p>
            <p>
                Crabtree now argues that their oral agreement entitles him to 20% of the profits generated by every
                single issue of the comic and 10% of the revenue generated by the TV series. Kirkman denies this
                claim, stating that Crabtree never had joint ownership of the copyright and any payments made to him
                before the claim were discretionary bonuses.
            </p>

            <h3 class="blog-content-subheading">The Outcome</h3>
            <p>
                The case is ongoing at the time of writing, meaning there’s no definitive outcome. But the case is
                worth highlighting because it raises some interesting questions about joint copyright ownership and
                its application. The key here is that having an agreement to receive a share of a copyrighted work’s
                revenue is not the same as having joint ownership of the copyright.
            </p>
            <p>
                As such, it’s possible that Crabtree is not a joint owner of *Invincible*. However, even that is
                complicated. Crabtree argues that his coloring work is inseparable from the story work that Kirkman
                contributed to the series. You can’t have one without the other, meaning he should be able to claim
                joint ownership. Kirkman’s counter to this is that Crabtree worked for hire and that another
                colorist could have done the same job with no negative consequences on the work.
            </p>
            <p>
                It’s a complicated case that may be in limbo for quite some time.
            </p>
            <p>
                But what it teaches us is that it’s vital to never assume anything when it comes to copyright.
                Contracts are necessary if your business embarks on a joint project with another party. These
                contracts must define how the copyright and revenue are shared, ensuring no confusion if the created
                work becomes popular. On the other hand, you should always be proactive when protecting your own
                copyright rights and never trust that others will have your best interests in mind on copyright
                ownership.
            </p>

            <hr>

            <h2 class="blog-content-heading">Tale #3: Shephard Fairey’s Obama poster</h2>
            <p>
                In 2008, a street artist named Shephard Fairey created the iconic “Hope” poster to represent
                then-candidate Barack Obama’s presidential campaign. The poster became iconic, turning into a symbol
                that achieved memetic status online. Though it was independent of Obama’s campaign, the poster also
                met with the future president’s approval.
            </p>
            <p>
                But there was a problem.
            </p>
            <p>
                The poster was not an entirely original work. It was based on a photograph taken by a freelancer,
                Mannie Garcia, which the Associated Press (AP) claimed was their copyrighted material.
            </p>

            <h3 class="blog-content-subheading">The Infringement</h3>
            <p>
                The AP argued that using Garcia’s photograph in the poster infringed on the copyright it held over
                the photo. They lodged a copyright claim stating they were due a portion of the poster’s revenue
                because of its use of AP-copyrighted material.
            </p>
            <p>
                For his part, Fairey claimed that he was protected from any copyright claims under the principle of
                fair use.
            </p>
            <p>
                Fair use protects creators that use other people’s work in certain circumstances, such as when they
                provide something of additional value or add insightful commentary to the existing work. Fairey
                argued that the unique coloring of the photo, coupled with the large HOPE slogan at the bottom, had
                a transformative effect on the photo.
            </p>
            <p>
                Fairey also claimed that Garcia’s photo may never have reached the level of exposure it had achieved
                thanks to his poster.
            </p>

            <h3 class="blog-content-subheading">The Outcome</h3>
            <p>
                This particular case never made it to court. Fairey and the AP arrived at a settlement in private,
                allowing the famous poster to become a cornerstone of the Obama campaign. Still, the case raises
                some interesting questions about fair use and its applications.
            </p>
            <p>
                Can something be considered fair use if the derivative work becomes more popular than the original?
            </p>
            <p>
                It’s an interesting question that raises plenty of legal ramifications. As a business owner, you can
                avoid issues related to fair use by always seeking the copyright holder’s permission to use an
                original work in your work. By ensuring the original copyright holder gets credit from the
                beginning, you avoid legal wranglings if your derivative work becomes part of the public zeitgeist.
            </p>

            <hr>

            <h2 class="blog-content-heading">Quick Tips for Avoiding Copyright Infringement</h2>
            <p>
                These cases all show how complicated copyright law can be. They also show us how copyright
                infringement can damage your business. Failure to account for copyright law in your work can lead to
                lengthy court proceedings and delays in publication, resulting in lost revenue. Applying these quick
                tips whenever you want to use somebody else’s work reduces your chances of facing a copyright
                infringement claim.
            </p>

            <h3 class="blog-content-subheading">Tip #1: Always Seek Permission and Give Credit</h3>
            <p>
                Even when you believe fair use laws cover you, it’s still a good idea to seek permission from the
                copyright owner (even if the work is posted on social media). Doing so allows you to form an
                agreement that clarifies where each party stands from the beginning. That agreement also means you
                have legal recourse if the other party launches an infringement case.
            </p>
            <p>
                Many copyright holders are happy for your business to use their work as long as they receive
                appropriate credit or attribution. Still, it depends on the copyright holder, so be prepared to
                offer monetary compensation.
            </p>

            <h3 class="blog-content-subheading">Tip #2: Never Assume An Original Work is Fair Game</h3>
            <p>
                Many business owners mistakenly assume that if somebody else’s original work does not carry a
                copyright notice, then it belongs to the public domain and is fair game to use. That isn’t the case.
                All original works are covered by copyright law, even if the work isn’t registered with the US
                Copyright Office. As such, never assume you can use somebody else’s work. Again, seek permission, or
                do not use the work unless you’re confident fair use laws protect you.
            </p>

