                What’s the difference between copyright, patent and trademark?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-35" hidden>
                <p>This is a common question, as each protects a different type of intellectual property:</p>

                <ul>
                    <li>A trademark generally protects a word, phrase, symbol and/or design that distinguishes the source of the goods — what we think of as brand name and brand recognition.</li>
                    <li>A patent generally protects an invention, including the functionality or design, or in other words, “any new and useful process, machine, manufacture, or composition of matter, or any new and useful improvement thereof.” 35 U.S.C. § 101.</li>
                    <li>Copyrights, meanwhile, generally protect artistic works such as books, photographs, arts, movies and music.</li>
