            <button class="accordion-header" aria-expanded="false" aria-controls="content-47">
                When do I need to register my copyright?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-47" hidden="">
                <p>
                    You can file at any time after you complete your work. However, there are certain advantages to timely registering your copyright in a timely fashion.
                </p>
                <ul>
                    <li>
                        Prima Facie Evidence: If you register before or within five years of publication, registration will establish *prima facie* evidence in court of the validity of the copyright and of the facts stated in the certificate.
                    </li>
                    <li>
                        Statutory Damages & Attorney’s Fees: If you register your work within three months after the publication or prior to an infringement of the work, statutory damages and attorney’s fees will be available to the copyright owner in court actions. Otherwise, only an award of actual damages and profits is available.
                    </li>
                </ul>
                <p>
                    In other words, if you wait until there is an infringement to register your work so you can have access to the courts, you may lose the valuable right to seek the recovery of statutory damages and attorneys’ fees.
                </p>
