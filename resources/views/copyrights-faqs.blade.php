            <button class="accordion-header" aria-expanded="false" aria-controls="content-48">
                What is fair use?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-48" hidden="">
                <p>
                    The Fair Use Doctrine allows you to use the copyrighted work of others in limited circumstances. Unfortunately, fair use is not an easy doctrine to understand or determine. If anyone has ever told you that as long you use no more than 20% of someone else’s work, or some other alleged bright line test, they are wrong.
                </p>
                <p>
                    Instead, fair use is a factually-specific inquiry and there is no bright line test. Courts consider these four factors:
                </p>
                <ol>
                    <li>The purpose and character of the use, including whether such use is of a commercial nature or is for nonprofit educational purposes;</li>
                    <li>The nature of the copyrighted work;</li>
                    <li>The amount and substantiality of the portion used in relation to the copyrighted work as a whole; and</li>
                    <li>The effect of the use upon the potential market for or value of the copyrighted work.</li>
                </ol>
                <p>
                    There are a lot of close calls when it comes to fair use and it is a defense to an alleged infringement.
                </p>
