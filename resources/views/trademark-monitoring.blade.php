                What does an applicant need to do to protect the mark after it is registered?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-9" hidden>
                <p>Once properly registered, trademark registrations generally need to be maintained by filing the appropriate forms at specific points in time. The deadlines are:</p>

                <ul>
                    <li>First Filing: The Declaration of Continued Use is due between the 5th and 6th anniversary date of the registration.</li>
                    <li>Second Filing: The combined Declaration of Use and Application for Renewal is due between the ninth and tenth year.</li>
                    <li>Subsequent Filings: The combined Declaration of Use and Application for Renewal is due every ten years thereafter.</li>
                </ul>

                <p>You may seek assistance through Trademark USP to help with your renewal filings.</p>

                <p>At the five year mark, assuming continued use of the mark, you may also pursue a “Declaration of Incontestability,” which provides additional protection under trademark law. This may prevent others from contesting the trademark on several common grounds, such as:</p>

                <ul>
                    <li>the mark is not inherently distinctive;</li>
                    <li>it is confusingly similar to a mark someone else began using first; or</li>
                    <li>the mark is simply functional as opposed to identifying the source of the goods or services.</li>
                </ul>
            </div>
