                <p>Yes, like any other business asset, a trademark can be sold, licensed or assigned. Validity requirements generally still apply, so someone usually needs to continue to use the mark in commerce and it is helpful to register assignments with the USPTO. </p>

                <p>If you enter into such an agreement, we can help with a transfer of the trademark with the PTO. Sometimes, individuals register the mark before a company is officially set up. Then, once the company is formed, the mark needs to be transferred into the company.</p>
