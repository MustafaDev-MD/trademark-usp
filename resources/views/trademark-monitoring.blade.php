        <div class="accordion-item">
            <button class="accordion-header" aria-expanded="false" aria-controls="content-10">
                Is it possible to make changes to the mark after it is filed?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-10" hidden>
                <p>It is possible to amend an application, but the process and the associated fees generally depend on the timing of the changes.</p>

                <ul>
                    <li>Before Publication: Changes before the trademark has been published in the Official Gazette can be made by logging into your Trademark USP account and requesting that a change be made. Additional fees and filing fees to the USPTO may apply.</li>
                    <li>After Publication: Changes once the trademark has been published in the Official Gazette generally require a Post-Publication Amendment, which you may request through Trademark USP.</li>
                </ul>

                <p>Depending on the scope of the amendment, an applicant may need to file a new application instead. Changing the mark entirely, for example, would require a new application. Broadening the scope of a description of goods/services or adding additional classes may require additional fees and would also likely require a new application. Clerical errors, on the other hand, are generally easy to fix and typically won’t affect the applicant’s place in line or require a new application.</p>
