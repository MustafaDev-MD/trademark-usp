                How does an applicant decide what description to use?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-4" hidden="">
                <p>
                    The trademark application includes a space for a description of goods and services. The description is usually at least two words but generally not more than a sentence that describes the goods or services.

                    Writing a description of use is sort of a Goldilocks and the Three Bears exercise. Too broad, and the application may get rejected if the USPTO thinks it infringes on another trademark when it really doesn’t. Too narrow, and the application may not reach the full range of a business’s offerings. The “just right” description of use typically meets the following criteria:
                <ul>
                    <li>Not too narrow so that some of the intended goods and services aren’t protected.</li>
                    <li>Not too broad so that the application gets rejected when it shouldn’t be.</li>
                    <li>Does not include an open ended list by using phrases such as “including,” etc.” and “such as.”.</li>
                    <li>Does not simply repeat the broader category name.</li>
                    <li>Does not use a term that fits into multiple categories (for example, “belts” could include tool belts in the Hand Tools Category and a suspender belt which would be in the Clothing Category).</li>
                    <li>Uses plain English recognizable and understandable outside a given industry.</li>
                    <li>Punctuation matters, so consider the difference between a semi-colon and a comma.</li>

