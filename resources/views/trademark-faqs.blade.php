            <button class="accordion-header" aria-expanded="false" aria-controls="content-10">
                Why would an applicant want to “disclaim” part of the trademark?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-10" hidden="">
                <p>
                    A mark is often (but not always) a group of words with one or more of them describing the product (e.g., tires) or it may be merely descriptive (e.g., great). While it may be appropriate to register the mark as a whole, the USPTO may question an attempt to claim some of the individual product or descriptive terms. Thus, some companies decide to disclaim those words without giving up on registering the mark as a whole or the USPTO may ask an applicant to do so after the fact.
                </p>
                <p>
                    The disclaimer does not remove the unregistrable part of a mark or affect its appearance. A disclaimer appears on the USPTO database that lets the world know the company is not making an exclusive claim to that particular part of the mark. Failure to disclaim part of a mark may be cause for rejection. Often times (but not always), the USPTO can issue an Action Letter seeking to disclaim the unregistrable parts of a mark, which may delay the application. At this point, there are generally two options: agree with the USPTO or decide to fight the request to disclaim. Because it may have significant legal ramifications on the protections associated with the mark, consider consulting with an attorney after receiving an Action Letter with a request to disclaim part of a mark.
                </p>
                <p>
                    The USPTO has published the following examples to illustrate when a disclaimer is likely required:
                </p>
                <ul>
                    <li>Merely Descriptive Words: Disclaiming “Creamy” from “Famous Mark Creamy Yogurt.”</li>
                    <li>Generic Words: Disclaiming “Yogurt” from “Famous Mark Creamy Yogurt.”</li>
                    <li>Geographic Terms: Disclaiming “Southwest” from “Famous Mark Southwest Guacamole.”</li>
                    <li>Informational Words: Disclaiming designations such as size of the product or the year of founding, which is usually applicable to registering logos.</li>
                    <li>Well-known Symbols: Disclaiming the “$” for a financial services company.</li>
                    <li>Misspellings from “telescoped words”: Disclaiming “Super” and “Rinse” from “Superinse Cleaner.” Another example is disclaiming “quick” from Jay’s Quik Print.</li>
                    <li>Compressed compound wording: Disclaim “Cool Pack.com” from the brand CoolPack.com because CoolPack is a compressed compound term and .com provides no indication of the source of the goods or services.</li>
                    <li>Foreign wording: If the translation of the foreign word is a generic or descriptive term, the USPTO says you disclaim it. The example they use is disclaiming “Rouge” from “Gala Rouge” for wine because Rouge just means “red,” which is descriptive of red wine.</li>
                    <li>Multiple words disclaimer: Some words just go together so the USPTO says they need to be disclaimed together instead of separately. The example they give is disclaiming “Pizza Parlor” from “Pete’s Pizza Parlor.”</li>
                </ul>
