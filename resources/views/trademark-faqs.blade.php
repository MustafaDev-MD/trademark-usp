            <button class="accordion-header" aria-expanded="false" aria-controls="content-12">
                What is the Supplemental Register?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-12" hidden="">
                <p>
                    For the most part, we have been talking about registering a mark on the Principal Register. The Supplemental Register is another option. If a mark is descriptive, a surname, or a geographic term, then it may need to be registered on the Supplemental Register. Often times, the USPTO will issue an Office Action suggesting filing on the Supplemental Register instead of the Principal Register.
                </p>
                <p>
                    So, what are the protections for trademarks on the Supplemental Register? Marks on the Supplemental Register generally can still:
                </p>
                <ul>
                    <li>Use the &reg; symbol</li>
                    <li>Put others on notice that the mark is already in use</li>
                    <li>Prevent others from obtaining confusingly similar marks from the USPTO - even on the Principal Register</li>
                </ul>
                <p>
                    Unfortunately, the registrant is generally not the presumed owner of a valid mark if there is a lawsuit. A Supplemental Register mark usually cannot be used to prevent the importation of counterfeit goods. Finally, Supplemental Register marks are typically not eligible to become incontestable like a mark on the Principal Register can. However, if the mark obtains secondary meaning, the mark can be refiled for acceptance on the Principal Register even if the mark is first filed on the Supplemental Register.
                </p>
            </div>
        </div>

        <div class="accordion-item">
            <button class="accordion-header" aria-expanded="false" aria-controls="content-13">
                How long does a trademark last?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-13" hidden="">
                <p>
                    Unlike copyrights and patents, trademark registrations can last forever under certain circumstances. Once properly registered, trademarks generally need to be maintained by filing the appropriate forms at specific points in time.
                </p>
                <ul>
                    <li>
                        The first filing required is the Declaration of Continued Use, which is due between the 5th and 6th anniversary date of the registration.
                    </li>
                    <li>
                        The next filing, the combined Declaration of Use and Application for renewal, is due between the ninth and tenth year.
                    </li>
                    <li>
                        After that, the combined Declaration of Use and Application for Renewal is due every ten years. You may seek assistance through Trademark USP to help with your renewal filings.
                    </li>
                </ul>
                <p>
                    At the five-year mark, assuming continued use of the mark, you may also pursue a “Declaration of Incontestability,” which provides additional protection under trademark law. This may prevent others from contesting the trademark on several common grounds, such as:
                </p>
                <ol>
                    <li>The mark is not inherently distinctive;</li>
                    <li>It is confusingly similar to a mark someone else began using first; or</li>
                    <li>The mark is simply functional as opposed to identifying the source of the goods or services.</li>
                </ol>
            </div>
        </div>

        <div class="accordion-item">
            <button class="accordion-header" aria-expanded="false" aria-controls="content-14">
                What do I do if someone is infringing?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-14" hidden="">
                <p>
                    Often a trademark owner will send a cease and desist letter demanding that the infringing party stop using the mark. The infringement may not be on purpose and the infringing party may stop when notified that their conduct is in clear violation of another’s rights.
                </p>
                <p>
                    The infringing party may also ignore the demand or take the position that they are not infringing. At that point, you may consider a trademark infringement lawsuit. You should talk with an attorney if you believe a lawsuit might be the right choice for you.
                </p>
                <p>
                    Successful lawsuits have common characteristics and you should discuss with a lawyer whether any of these are present in your situation:
                </p>
                <ul>
                    <li>The plaintiff is the owner of a valid mark - which is ordinarily presumed if they have a federal trademark registration</li>
                    <li>The other side is using the mark without authority in connection with the sale of goods or services</li>
                    <li>The other side is using the mark in a manner that is likely to cause confusion to reasonable consumers</li>
                </ul>
            </div>
        </div>

        <div class="accordion-item">
            <button class="accordion-header" aria-expanded="false" aria-controls="content-15">
                What if the trademark office objects to an application?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-15" hidden="">
                <p>
                    The USPTO may object to the application or send what is referred to as an Office Action or Action Letter.
                </p>
                <p>
                    This can happen for a variety of reasons, including for small administrative matters, such as a clarification of the goods and services being provided, or for more substantive issues such as when the USPTO determines the requested mark is too similar to one already in existence or the mark is too generic or merely descriptive.
                </p>
                <p>
                    Applicants have the right to respond to the trademark office to address small administrative matters and/or make the case as to why any substantive issues raised in the Office Action can be overcome. You should consider obtaining counsel to address an Office Action if you've received one. You may request assistance in such matters from the attorneys of Swyft Legal through our website.
                </p>
            </div>
        </div>
        <div class="accordion-item">
            <button class="accordion-header" aria-expanded="false" aria-controls="content-16">
                Is it possible to make changes to the mark after it is filed?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-16" hidden="">
                <p>
                    It is possible to amend an application, but the process and the associated fees generally depend on the timing of the changes.
                </p>
                <ul>
                    <li>
                        Before publication in the Official Gazette: Changes can be made by logging into your Trademark USP account and requesting that a change be made. Additional fees and filing fees to the USPTO may apply.
                    </li>
                    <li>
                        After publication in the Official Gazette: Changes generally require a Post-Publication Amendment, which you may request through Trademark USP.
                    </li>
                </ul>
                <p>
                    When a New Application is Required: Depending on the scope of the amendment, an applicant may need to file a new application instead. Changing the mark entirely, for example, would require a new application. Broadening the scope of a description of goods/services or adding additional classes may require additional fees and would also likely require a new application.
                </p>
                <p>
                    Clerical errors, on the other hand, are generally easy to fix and typically won’t affect the applicant’s place in line or require a new application.
                </p>
            </div>
        </div>
        <div class="accordion-item">
            <button class="accordion-header" aria-expanded="false" aria-controls="content-17">
                Why might a mark get rejected?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-17" hidden="">
                <p>
                    A common basis for rejection is that the mark’s name is too similar to a name already registered with the USPTO.
                </p>
                <p>
                    A mark can also be rejected for the following reasons:
                </p>
                <ul>
                    <li>The name is too generic or descriptive.</li>
                    <li>It is a surname.</li>
                    <li>It is geographically descriptive of the location of the business.</li>
                    <li>It is a foreign term that translates to a generic or descriptive English term.</li>
                    <li>It uses an individual’s name or likeness.</li>
                    <li>It is the title of a single book or movie.</li>
                </ul>
            </div>
        </div>

        <div class="accordion-item">
            <button class="accordion-header" aria-expanded="false" aria-controls="content-18">
                Is it possible to sell, license, or assign a trademark?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-18" hidden="">
                <p>
                    Yes, like any other business asset, a trademark can be sold, licensed, or assigned.
                </p>
                <p>
                    Validity requirements generally still apply, so someone usually needs to continue to use the mark in commerce and it is helpful to register assignments with the USPTO. If you enter into such an agreement, we can help with a transfer of the trademark with the PTO.
                </p>
                <p>
                    Sometimes, individuals register the mark before a company is officially set up. Then, once the company is formed, the mark needs to be transferred into the company.
                </p>
            </div>
        </div>

        <div class="accordion-item">
            <button class="accordion-header" aria-expanded="false" aria-controls="content-19">
                Is software a good or service?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-19" hidden="">
                <p>
                    According to the USPTO, software can be both a good and service.
                </p>
                <ul>
                    <li>
                        If a company is selling actual software purchased by customers through a disk or download, then the description may be the product description “Computers and Scientific Devices.”
                    </li>
                    <li>
                        If a company is selling SaaS (Software as a Service) from a website, then the description may be a service of “Science and Technology Services.”
                    </li>
                </ul>
            </div>
        </div>

        <div class="accordion-item">
            <button class="accordion-header" aria-expanded="false" aria-controls="content-20">
                Is it possible to trademark a fashion brand?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-20" hidden="">
                <p>
                    Registering a clothing brand may protect it from others selling clothes under the same name or logo.
                </p>
                <p>
                    The USPTO, however, may not approve an application for clothing that is simply a slogan slapped on a t-shirt. Ordinarily, the word or phrase should actually be representative of a brand and not an effort to monopolize a slogan.
                </p>
                <p>
                    The USPTO usually asks for evidence that the words are being used as a brand by asking for the tags or coat hangers with the brand name on it.
                </p>
                <p>
                    Fashion brands may start with Class 25 for clothing before branching out to areas like jewelry or handbags, which go into different classes and require additional USPTO filing fees.
                </p>
            </div>
        </div>

        <div class="accordion-item">
            <button class="accordion-header" aria-expanded="false" aria-controls="content-21">
                Is it possible to trademark the title to a book, characters or other written works?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-21" hidden="">
                <p>
                    When it comes to books or other literary works,copyright usually applies instead of trademark. Copyright generally keeps people from copying a story. It protects the creative, written work.
                </p>
                <p>
                    A trademark ordinarily identifies the source of goods or services. Therefore, trademark does not apply to a single book or story.
                </p>
                <p>
                    However:
                </p>
                <ul>
                    <li>A series of books (like the Hardy Boys for example) may be trademarked so that a consumer knows that the individual book is part of that series.</li>
                    <li>Characters can be trademarked as it relates to products associated with the books. In other words, before trademarking an individual story or character, there needs to be merchandise or services (which includes shows and appearances) related to that story or character.</li>
                </ul>
            </div>
        </div>

        <div class="accordion-item">
            <button class="accordion-header" aria-expanded="false" aria-controls="content-22">
                Can a person trademark their given, birth or surname?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-22" hidden="">
                <p>
                    Generally, the USPTO discourages trademarking a given name, or even a stage name, to prevent the exclusive right to use a name.
                </p>
                <p>
                    An individual name may be trademarked, however, if the name has garnered a secondary meaning through its connection to a good or service—and obviously, if it belongs to the person seeking the trademark.
                </p>
                <p>
                    Secondary meaning means that it is famous. For example, Cher and Michael Jordan have registered trademarks in their names. But John Smith, who works as an accountant, likely won’t be able to trademark that name.
                </p>
            </div>
        </div>

        <div class="accordion-item">
            <button class="accordion-header" aria-expanded="false" aria-controls="content-23">
                Can a band trademark its name?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-23" hidden="">
                <p>
                    The most common question we receive about bands or other musicians is what class should be used? If the budget permits it, many bands will register in several classes, including entertainment services, sound recordings, and clothing.
                </p>
                <p>
                    Each of those costs at least $350 in USPTO filing fees, so it may not be an economical option initially. For those just starting out, consider registering entertainment services (Class 41) to try and protect the mark as it relates to live performances and begin building the fan base. As a band grows, it can then afford to start thinking about the “merch.” This seems to be the most typical path taken by musicians.
                </p>
                <p>
                    By way of example, the Dave Matthews Band did the following registrations in this order:
                </p>
                <ul>
                    <li>1993 – Class 41 Entertainment Services</li>
                    <li>1995 – Class 9 Sound Recordings</li>
                    <li>1997 – Class 25 Clothing</li>
                    <li>1997 - Class 16 Posters and other paper goods</li>
                    <li>1998 – Class 42 Interactive computer services for fan group</li>
                    <li>1999 – Class 26 Cloth patches</li>
                    <li>2011 – Class 18 Backpacks</li>
                    <li>2011 – Class 24 Blankets</li>
                    <li>2011 – Class 14 Jewelry</li>
                </ul>
                <p>
                    Had they done it all at once, the filing fees alone would have cost at least $2,475 and it is not likely they were selling most of this stuff when they first started. The USPTO generally requires a specimen, which is proof a company is selling the identified goods or services in commerce, unless an intent to use application is used instead.
                </p>
            </div>
        </div>
        <div class="accordion-item">
            <button class="accordion-header" aria-expanded="false" aria-controls="content-25">
                Can a nonprofit, social media group or social club obtain a trademark for its name?
                <span class="accordion-icon">+</span>
            </button>
            <div class="accordion-content" id="content-25" hidden="">
                <p>
                    The name, logo, and slogan of a nonprofit or social group (even if it is just done through social media) may be eligible for a trademark. Registering the mark generally prohibits others from using it or prevents an unauthorized use that does not align with the social mission or charitable functions of the nonprofit. An online or offline community generally can be protected by a trademark.
                </p>
                <p>
                    However, the use of social media to sell or promote other goods or services is generally not a mark that can be registered. Instead, filing the proper trademark that relates to those goods or services and not trying to describe it as a community built around the goods or services has worked for some.
                </p>
                <p>
                    Some non-profits get confused by the “use in commerce” requirement. For non-profits, the equation is a little different. One person, such as a homeless person, may receive the goods or services from the non-profit and the non-profit may receive funds from a different person. This is generally considered sufficient to satisfy the “use in commerce” requirement, but the USPTO will want proof of non-profit status. Social organizations, meanwhile, typically use their marks in commerce when there are dues or monies spent on activities.
                </p>
                <p>
                    Some groups have registered under:
                </p>
                <ul>
                    <li>Class 35 (Advertisement and Business Services) as promoting causes;</li>
                    <li>Class 36 for charitable fund raising services;</li>
                    <li>Class 41 (Education and Entertainment) as social club services that arrange, organize, and host social events, get-togethers, and parties for members or gather and share information about certain causes.</li>
                </ul>
            </div>
